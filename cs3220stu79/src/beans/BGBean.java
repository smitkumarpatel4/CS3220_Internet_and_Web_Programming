package beans;

public class BGBean {
	
	int red, green, blue;
	public BGBean(){
		this.red=255;
		this.green=0;
		this.blue=0;
		
		
	}
	public int getR() {
		return red;
	}
	public void setR(int red) {
		this.red = red;
	}
	public int getG() {
		return green;
	}
	public void setG(int green) {
		this.green = green;
	}
	public int getB() {
		return blue;
	}
	public void setB(int blue) {
		this.blue = blue;
	}


}
